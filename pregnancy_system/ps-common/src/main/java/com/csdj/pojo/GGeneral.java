package com.csdj.pojo;

/**
 * 一般情况表
 */
public class GGeneral {
    private Integer id;//一般情况id
    private Integer anyDisease;//是否有疾病（1代表有，2代表没有）
    private Integer aidId;//疾病表id（外键 对应疾病表）
    private Integer birthDefects;//是否有出生缺陷（1代表有，2代表没有）
    private String birthDefectsContent;//具体缺陷病名内容
    private Integer isDisease;//是否有妇科（男性）疾病（1代表有，2代表没有）
    private Integer isDiseaseId;//妇科(男性)疾病表id
    private Integer atPresent;//目前是否服药（1代表有，2代表没有）
    private String drugName;//药物名称
    private Integer vaccinationorNot;//是否注入疫苗（1代表有，2代表没有）
    private Integer vaccinationorNotId;//疫苗表id
    private Integer eid;//检查医生与检查时表id（外键 对应检查医生与检查时间表）

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAnyDisease() {
        return anyDisease;
    }

    public void setAnyDisease(Integer anyDisease) {
        this.anyDisease = anyDisease;
    }

    public Integer getAidId() {
        return aidId;
    }

    public void setAidId(Integer aidId) {
        this.aidId = aidId;
    }

    public Integer getBirthDefects() {
        return birthDefects;
    }

    public void setBirthDefects(Integer birthDefects) {
        this.birthDefects = birthDefects;
    }

    public String getBirthDefectsContent() {
        return birthDefectsContent;
    }

    public void setBirthDefectsContent(String birthDefectsContent) {
        this.birthDefectsContent = birthDefectsContent;
    }

    public Integer getIsDisease() {
        return isDisease;
    }

    public void setIsDisease(Integer isDisease) {
        this.isDisease = isDisease;
    }

    public Integer getIsDiseaseId() {
        return isDiseaseId;
    }

    public void setIsDiseaseId(Integer isDiseaseId) {
        this.isDiseaseId = isDiseaseId;
    }

    public Integer getAtPresent() {
        return atPresent;
    }

    public void setAtPresent(Integer atPresent) {
        this.atPresent = atPresent;
    }

    public String getDrugName() {
        return drugName;
    }

    public void setDrugName(String drugName) {
        this.drugName = drugName;
    }

    public Integer getVaccinationorNot() {
        return vaccinationorNot;
    }

    public void setVaccinationorNot(Integer vaccinationorNot) {
        this.vaccinationorNot = vaccinationorNot;
    }

    public Integer getVaccinationorNotId() {
        return vaccinationorNotId;
    }

    public void setVaccinationorNotId(Integer vaccinationorNotId) {
        this.vaccinationorNotId = vaccinationorNotId;
    }

    public Integer getEid() {
        return eid;
    }

    public void setEid(Integer eid) {
        this.eid = eid;
    }
}
