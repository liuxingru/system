package com.csdj.pojo;

/**
 * 同意类型表
 */
public class Consent {
    private Integer cid;//类型ID
    private String cname;//类型名称

    public Integer getCid() {
        return cid;
    }

    public void setCid(Integer cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }
}
